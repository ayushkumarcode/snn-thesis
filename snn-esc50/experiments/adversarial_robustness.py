"""
adversarial_robustness.py -- FGSM + PGD adversarial attack comparison: SNN vs ANN.

Research question: Does binary spike thresholding naturally filter adversarial
perturbations on mel spectrograms?

Method:
  - FGSM and PGD attacks at epsilon in {0.0, 0.01, 0.02, 0.05, 0.1, 0.2, 0.3}
  - Both SNN (direct encoding, best_fold4.pt) and ANN (best_fold4.pt)
  - fold 4 test set (400 samples)
  - Report accuracy vs epsilon curve for both models

Key citation:
  - Sharmin et al. ECCV 2020 (arXiv:2003.10399): SNNs with Poisson encoding
    show higher adversarial accuracy in black-box scenarios.
  - IMPORTANT: Must use PGD with >=40 steps for reliable evaluation of SNNs.
    Standard PGD underestimates SNN robustness due to vanishing surrogate
    gradients (arXiv:2512.22522, 2024).

Usage:
  source .venv/bin/activate
  cd snn-esc50/
  python experiments/adversarial_robustness.py
  python experiments/adversarial_robustness.py --num-samples 50 --fold 4
"""

import argparse
import json
import sys
from pathlib import Path

_REPO_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(_REPO_ROOT))

import torch
import torch.nn as nn
import numpy as np
import torchattacks

from src.config import RESULTS_DIR, NUM_CLASSES, NUM_STEPS, BETA
from src.models.snn_model import SpikingCNN
from src.models.ann_model import ConvANN
from src.encoding import encode_direct
from src.dataset import get_fold_dataloaders


# ============================================================
# SNN wrapper for torchattacks
# ============================================================

class SNNWrapper(nn.Module):
    """Wraps SNN + direct encoder so torchattacks can attack it.

    Forward pass:
      x (B, 1, H, W) → encode (T, B, 1, H, W) → SNN → mem_out.sum(0) (B, 50)

    The gradient of loss w.r.t. x flows back through the sum and through all
    T timesteps of the SNN (via surrogate gradient). This is valid for
    adversarial example generation.

    Note: We use mem_out.sum(0) as logits (not spk_out.sum(0)) because
    membrane potential has smoother gradients than spike counts.
    """

    def __init__(self, snn_model: SpikingCNN, num_steps: int = NUM_STEPS):
        super().__init__()
        self.snn = snn_model
        self.T = num_steps

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        Args:
            x: (batch, 1, H, W) mel spectrogram in [0, 1]
        Returns:
            logits: (batch, num_classes) summed membrane potentials
        """
        spk_input = encode_direct(x, num_steps=self.T)
        spk_out, mem_out = self.snn(spk_input)
        return mem_out.sum(dim=0)  # (batch, 50)


# ============================================================
# Evaluation helpers
# ============================================================

def evaluate_clean(model: nn.Module, loader, device: str) -> float:
    """Evaluate model on clean (unperturbed) data."""
    model.eval()
    correct = 0
    total = 0
    with torch.no_grad():
        for data, targets in loader:
            data, targets = data.to(device), targets.to(device)
            logits = model(data)
            correct += (logits.argmax(1) == targets).sum().item()
            total += targets.size(0)
    return correct / total


def evaluate_adversarial(model: nn.Module, attack, loader,
                          device: str, max_samples: int) -> float:
    """Evaluate model on adversarial examples generated by attack."""
    model.eval()
    correct = 0
    total = 0

    for data, targets in loader:
        if total >= max_samples:
            break
        data, targets = data.to(device), targets.to(device)

        # Generate adversarial examples
        adv_data = attack(data, targets)

        with torch.no_grad():
            logits = model(adv_data)
            correct += (logits.argmax(1) == targets).sum().item()
            total += targets.size(0)

    return correct / total if total > 0 else 0.0


# ============================================================
# Main
# ============================================================

def main():
    parser = argparse.ArgumentParser(
        description="FGSM + PGD adversarial robustness: SNN vs ANN"
    )
    parser.add_argument("--fold", type=int, default=4,
                        help="Test fold (default: 4)")
    parser.add_argument("--num-samples", type=int, default=400,
                        help="Number of test samples to attack (default: 400 = full fold)")
    parser.add_argument("--device", default=None,
                        help="Device (default: auto)")
    args = parser.parse_args()

    # Device
    if args.device:
        device = args.device
    elif torch.cuda.is_available():
        device = "cuda"
    elif hasattr(torch.backends, "mps") and torch.backends.mps.is_available():
        device = "mps"
    else:
        device = "cpu"

    print("=" * 60)
    print("Adversarial Robustness: SNN vs ANN")
    print("=" * 60)
    print(f"  Fold    : {args.fold}")
    print(f"  Samples : {args.num_samples}")
    print(f"  Device  : {device}")
    print()

    # Load models
    snn_path = RESULTS_DIR / "snn" / "direct" / f"best_fold{args.fold}.pt"
    ann_path = RESULTS_DIR / "ann" / "none" / f"best_fold{args.fold}.pt"

    for p in [snn_path, ann_path]:
        if not p.exists():
            print(f"FATAL: Model not found: {p}")
            sys.exit(1)

    snn_model = SpikingCNN().to(device)
    snn_model.load_state_dict(torch.load(snn_path, map_location=device, weights_only=True))
    snn_model.eval()
    print(f"Loaded SNN: {snn_path}")

    ann_model = ConvANN().to(device)
    ann_model.load_state_dict(torch.load(ann_path, map_location=device, weights_only=True))
    ann_model.eval()
    print(f"Loaded ANN: {ann_path}")

    # SNN wrapper for attacks
    snn_wrapper = SNNWrapper(snn_model, num_steps=NUM_STEPS).to(device)

    # Data loader
    _, test_loader = get_fold_dataloaders(args.fold, batch_size=32, augment=False)

    # Epsilon values to test
    epsilons = [0.0, 0.01, 0.02, 0.05, 0.1, 0.2, 0.3]

    results = {
        "fold": args.fold,
        "num_samples": args.num_samples,
        "device": device,
        "epsilons": epsilons,
        "fgsm": {"snn": [], "ann": []},
        "pgd": {"snn": [], "ann": []},
    }

    print()
    print(f"{'eps':>6}  {'FGSM SNN':>10}  {'FGSM ANN':>10}  "
          f"{'PGD SNN':>9}  {'PGD ANN':>9}")
    print("-" * 55)

    for eps in epsilons:
        if eps == 0.0:
            # Clean accuracy
            acc_snn_fgsm = evaluate_clean(snn_wrapper, test_loader, device)
            acc_ann_fgsm = evaluate_clean(ann_model, test_loader, device)
            acc_snn_pgd = acc_snn_fgsm
            acc_ann_pgd = acc_ann_fgsm
        else:
            # FGSM
            fgsm_snn = torchattacks.FGSM(snn_wrapper, eps=eps)
            fgsm_ann = torchattacks.FGSM(ann_model, eps=eps)
            acc_snn_fgsm = evaluate_adversarial(
                snn_wrapper, fgsm_snn, test_loader, device, args.num_samples)
            acc_ann_fgsm = evaluate_adversarial(
                ann_model, fgsm_ann, test_loader, device, args.num_samples)

            # PGD (40 steps for reliable SNN evaluation per arXiv:2512.22522)
            pgd_snn = torchattacks.PGD(snn_wrapper, eps=eps,
                                        alpha=eps / 10, steps=40)
            pgd_ann = torchattacks.PGD(ann_model, eps=eps,
                                        alpha=eps / 10, steps=40)
            acc_snn_pgd = evaluate_adversarial(
                snn_wrapper, pgd_snn, test_loader, device, args.num_samples)
            acc_ann_pgd = evaluate_adversarial(
                ann_model, pgd_ann, test_loader, device, args.num_samples)

        results["fgsm"]["snn"].append(float(acc_snn_fgsm))
        results["fgsm"]["ann"].append(float(acc_ann_fgsm))
        results["pgd"]["snn"].append(float(acc_snn_pgd))
        results["pgd"]["ann"].append(float(acc_ann_pgd))

        print(f"{eps:>6.3f}  {acc_snn_fgsm:>10.4f}  {acc_ann_fgsm:>10.4f}  "
              f"{acc_snn_pgd:>9.4f}  {acc_ann_pgd:>9.4f}")

    # Save results
    out_dir = RESULTS_DIR / "adversarial"
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"robustness_fold{args.fold}.json"

    with open(out_path, "w") as f:
        json.dump(results, f, indent=2)

    print()
    print(f"Results saved: {out_path}")
    print()
    print("Interpretation:")
    clean_snn = results["fgsm"]["snn"][0]
    clean_ann = results["fgsm"]["ann"][0]
    eps_01_snn = results["fgsm"]["snn"][epsilons.index(0.1)] if 0.1 in epsilons else None
    eps_01_ann = results["fgsm"]["ann"][epsilons.index(0.1)] if 0.1 in epsilons else None
    print(f"  Clean accuracy: SNN={clean_snn:.2%}, ANN={clean_ann:.2%}")
    if eps_01_snn is not None:
        print(f"  At eps=0.1 (FGSM): SNN={eps_01_snn:.2%}, ANN={eps_01_ann:.2%}")
        gap_clean = clean_ann - clean_snn
        gap_eps01 = eps_01_ann - eps_01_snn
        drop_snn = clean_snn - eps_01_snn
        drop_ann = clean_ann - eps_01_ann
        print(f"  Accuracy drop to eps=0.1: SNN drops {drop_snn:.2%}, ANN drops {drop_ann:.2%}")
        if drop_snn < drop_ann:
            print("  FINDING: SNN is MORE robust to FGSM attacks.")
        else:
            print("  FINDING: ANN is MORE robust to FGSM attacks (challenges prior assumption).")


if __name__ == "__main__":
    main()
